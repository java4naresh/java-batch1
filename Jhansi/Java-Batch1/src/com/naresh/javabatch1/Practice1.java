package com.naresh.javabatch1;

public class Practice1 {
	public static void main(String[] args) {

		int Unit = 12;
		if (Unit <= 10) {
			System.out.println(Unit * 10);
		} else if (Unit > 10 && Unit <= 15) {
			System.out.println(Unit * 15);
		} else if (Unit > 15 && Unit <= 20) {
			System.out.println(Unit * 20);
		} else {

			System.out.println(Unit * 30);
		}

	}

}
